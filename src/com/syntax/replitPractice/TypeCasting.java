package com.syntax.replitPractice;

public class TypeCasting {

	public static void main(String[] args) {
		/*
		 * 18-->For you to do:
		 * Write a program to convert double 500.4444 into long.
		 * 
		 * Expected Output:
		 * 
		 * 500
		 */
		double a =500.4444;
	    long b =(long)a;
	    System.out.println(b);
	    
	    System.out.println("----------------------------------");
	    
		/*
		 * 19-->For you to do:
		 * Write a program to convert double 100.50 into int type
		 * 
		 * Expected Output:
		 * 
		 * 100
		 */
	    double c=100.50;
	    int d=(int)c;
	    System.out.println(d);
	}

}
