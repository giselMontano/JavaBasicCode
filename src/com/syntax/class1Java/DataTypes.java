package com.syntax.class1Java;

public class DataTypes {

	public static void main(String[] args) {
		
// to create a variable: datatype and name 
		byte box1 = 127;
		short box2 = 2022;
		int box3 = 76666;
		long box4 = 45678990907649L;
		
		System.out.println("---------------------------------------------------------------------------------------------------");

//decimal values
		float container = 1.99f;
		double container2 = 89.99;
		
		System.out.println("---------------------------------------------------------------------------------------------------");

//to represent 1 character
		char gender = 'm';
		char dollar = '$';
		
		System.out.println("---------------------------------------------------------------------------------------------------");

//to represent true or false (yes or no)
		boolean hungry = true;
		boolean sleepy = false;
		
		System.out.println("---------------------------------------------------------------------------------------------------");

//how to access values from variables example:

		System.out.println(container);
		
		System.out.println("---------------------------------------------------------------------------------------------------");

//homework access and print from all variables 

		System.out.println(box1);
		System.out.println(box2);
		System.out.println(box3);
		System.out.println(box4);
		System.out.println(container);
		System.out.println(container2);
		System.out.println(gender);
		System.out.println(dollar);
		System.out.println(hungry);
		System.out.println(sleepy);
	}

}
